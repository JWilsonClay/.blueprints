"""Git Bridge – version control for agentic changes."""

import subprocess
from pathlib import Path
from datetime import datetime

def commit_changes(message: str, files: list = None):
    if files is None:
        files = ["."]
    subprocess.run(["git", "add"] + files, check=True)
    subprocess.run(["git", "commit", "-m", f"[AGENTIC] {message}\n\nGenerated by Orchestrator at {datetime.now().isoformat()}"], check=True)
    print(f"✅ Committed: {message}")

def rollback_to_last_good_commit():
    subprocess.run(["git", "reset", "--hard", "HEAD~1"], check=True)
    print("🔄 Rolled back to last good commit")

def generate_changelog():
    with open("CHANGELOG.md", "a") as f:
        f.write(f"\n## {datetime.now().date()} – Agentic Update\n- Auto-committed by git_bridge\n")
    print("📝 CHANGELOG.md updated")

if __name__ == "__main__":
    commit_changes("Test commit from runtime toolkit")
    generate_changelog()